#include <bits/stdc++.h>
using namespace std;

string Add( string a , string b )
{
	reverse( a.begin() , a.end() );
	reverse( b.begin() , b.end() );
	string ans(256,'0');
	int value = 0 , carry = 0;
	int i , j , alen = a.length() , blen = b.length();
	if( alen < blen ){
		swap( a , b );
		swap( alen , blen ); //cout << a << b << endl;
	}
	for( i = 0 ; i < alen ; i++ ){
		if( i >= blen ){
			value = (a[i]-'0') + carry;
			carry = value / 10;
			ans[i] = value % 10 + '0';
			continue;
		}
		value = (a[i]-'0') + (b[i]-'0')+carry;
		carry = value/10;
		ans[i] = value % 10 + '0';
	}
	if( carry ){
		ans[i] = carry + '0';
	}
	reverse( ans.begin() , ans.end() );
	while ( ans[0] == '0' ) ans.erase( 0, 1 );
	return ans;
}

bool isH( string s ){
	string ans = s;
	reverse( ans.begin() , ans.end() );
	return ans == s;
 }

int main(){
	string s;
	cin >> s;
	s = Add( s , "1" );
	while ( 1 ){
		if( isH( s ) ){
			cout << s << endl;
			break;
		}
		s = Add( s , "1" );
	}
} 
